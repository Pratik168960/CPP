// ******** FOR LOOP ********

#include<iostream>
using namespace std;

int main (){
    int n;
    cout << "Enter a value of n : ";
    cin >> n;

    cout << "printing count from 1 to n :" << endl;
    for (int i = 1; i <= n; i++){
        cout << i << endl;
    }

// for (initialisation; condition; updation inc/dec)
// Initialization (once) → Condition Check → Execute Body → 
// Update → Repeat from Condition Check (until false).
}